SELECT DISTINCT CASE WHEN CAT_MAIN.f$Syscode=3301 THEN 1 ELSE 0 END AS SYS
, CONVERT(VARCHAR(512), CONVERT(BIGINT, CATALOGS.f$NREC)+9223372036854775808) AS ID
, CATALOGS.f$CODE AS CODE
, CATALOGS.f$NAME AS NAME
FROM t$CATALOGS CAT_MAIN
LEFT OUTER
JOIN t$CATALOGS CATALOGS ON
CATALOGS.f$CPARENT=CAT_MAIN.f$NREC
WHERE
 (
(CAT_MAIN.f$SYSCODE=3302 OR CAT_MAIN.f$SYSCODE=3301)
)