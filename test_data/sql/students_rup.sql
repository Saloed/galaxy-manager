SELECT CONVERT(VARCHAR(512), CONVERT(BIGINT,Curric.F$nREc)- CONVERT(BIGINT,0x8000000000000000)) AS RUPNREC
,Curric.f$YEARED AS RYear
FROM t$U_student UStudent
JOIN T$U_CURR_GROUP Grup ON Grup.F$CSTGR = UStudent.F$CSTGR
JOIN T$U_CURRICULUM Curric ON Curric.F$CPARENT = Grup.F$CCURR
WHERE UStudent.f$nREc = CONVERT(BINARY(8), %s, 1)
